                    <h1>Широкоформатная печать</h1>

                     <b>Широкоформатная печать</b> – это способ нанесения полноцветных изображений на специальные рекламные материалы. Материалами для широкоформатной печати являются : самоклеющаяся плёнка, баннерная ткань, а также бумага blueback и citylight.<br>
                     Благодаря инновационным технологиям, сейчас широкоформатная печать  отличается прекрасным качеством, ярким, реальным изображением любого размера.
                     <br><br>
                     Условно широкоформатную печать можно разделить на две категории по области применения: это наружная реклама и интерьерная печать. Они отличаются способом нанесения изображения, рекламным носителем и, что главное, качеством печати (разрешением). Так, например, для наружной рекламы вполне достаточно разрешения 370 dpi (точек на дюйм), при интерьерной же печати необходимо гораздо большее качество печати.

                     <br><br>
                     <b>360 dpi – экстерьерная печать, дешевая 360 dpi 2-4 pass</b><br>
                     Для наружного и внутреннего применения.<br>
                     Крупное зерно печати, возможны тонкие незапечатанные полосы. Недостатки скрываются на расстоянии 3-5 метров.<br>
                     Применение: билборды, брандмауэры, банер сетка, изделия больших размеров не требующие детализации печати.<br>
                     <br>
                     <b>720 dpi – экстерьерно печать, обычная 720 dpi 4-6 pass</b><br>
                     Для наружного и внутреннего применения.<br>
                     Обычная печать. Красивая, яркая, слабо заметно зерно и нечеткость изображения. <br>
                     Применение: 80% продукции, печатают именно этим качеством.<br>
                     <br>
                     <b>1440 dpi – Интерьерная печать, хорошая 1440 dpi 8-12 pass</b><br>
                     Для наружного и внутреннего применения.<br>
                     Это наилучшее качество. Яркая и реалистичная печать, максимальная четкость изображения, пропечатка 10п шрифта. <br>
                     Применение: наклейки, плакаты, оформление торговых залов.<br><br>

