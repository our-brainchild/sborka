                    <h1>Материалы и технология изготовления пластиковых карт</h1>

                    <img style="float: left; padding-right: 15px; width: 250px; height: 370px; " src="../images/modal/plast-material-1.jpg">



                    Самым популярным материалом, из чего делают пластиковых карты, остается поливинилхлорид (ПВХ). Такой материал очень хорошо обрабатывается и поддается окрашиванию. Поэтому во всем мире кредитные банковские карты изготавливают в основном из этого материала. ПВХ нейтрален к красящим веществам – это особенно благоприятно при цветной печати карт, когда цвета сохраняют свои начальные значения.<br><br>

                    Пластиковые карты делаются так: сначала с помощью обычного компьютера
                    печатаются макеты на специальном пластике на струйном принтере. Для печати пластиковых карт можно использовать пластик разных цветов - белый, серебряный, золотой.<br><br>

                    Если на пластиковой карте нужны переменные данные: ФИО, номера карт, коды
                    доступа, логины, пароли, штрих коды, то все это впечатывают сразу вместе с
                    макетом. <br><br>

                    После печати, так же, как и обычный бутерброд, собирают пластиковую карту:
                    защитная пленка, лицевая часть, оборотная часть и снова защитная пленка. После
                    сборки все эти слои спекаются под давлением и температурой в пресс-печке, после
                    чего карты вырубаются специальным вырубщиком пластиковых карт. Затем, если это
                    кредитная карта или просто карта, которая должна содержать записанную на ней
                    информацию, то кодируются ее магнитная полоса и на ней делают эмбоссирование
                    (это такие выпуклые символы на карте).
