                     <br>
                     <b>Уличное размещение</b> подразумевает использование материала в разных погодных условиях в том числе при ветре и дожде, а значит к таким материалам и их характеристикам применяются повышенные требования на прочность. К таким материалам можно отнести ламинированный баннер, баннерная сетка.<br>
