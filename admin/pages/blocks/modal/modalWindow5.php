                    <h1>Офсетная печать «Сборных спусков»</h1>



                     <b>Сборный спуск – это печать спуска с одинаковыми параметрами:</b><br>
                     Материал – бумага определённой плотности.<br>
                     Покрытие – без покрытия или с ламинацией, лаком.<br>
                     Печать - печать как с одной (4+0) так и двух сторон (4+4).<br>
                     <br>

                     <table><tbody><tr><td style="width:70px; vertical-align: top;">
                     <i>Пример 1:</i>
                     </td><td><i>

                     <b>Сборный спуск - Самоклеящаяся бумага Raflatak, печать 4+0</b><br>
                     Материал – самоклеящаяся бумага Raflatak<br>
                     Покрытие – нет <br>
                     Печать – 4+0 (возможна печать только с одной стороны)<br>
                     </i>
                     </td></tr></tbody></table>

                      <br>
                     <table><tbody><tr><td style="width:70px; vertical-align: top;">
                     <i>Пример 2:</i>
                     </td><td><i>

                     <b>Сборный спуск - Мелованная бумага 350 гр/м2 + Глянцевая ламинация по лицевой стороне, печать 4+4</b><br>
                     Материал – Мелованная бумага 350 гр/м2<br>
                     Покрытие – глянцевая ламинация по лицевой стороне (ламинация 1+0)<br>
                     Печать – 4+4 (возможна печать как с одной, так и двух сторон)<br>
                     </i>
                     </td></tr></tbody></table>
                     <br>
